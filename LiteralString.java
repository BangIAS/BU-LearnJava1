public class LiteralString{
    public static void main(String[] args) {
        System.out.println("abc \ndef"); //"\n" Merupakan Karakter Escape yang berarti "Newline" atau "Enter"
        System.out.println("abc \t def"); //"\t" Merupakan Karakter Escape yang berarti "Tab"
        System.out.println("\" Halo \""); // Memeberikan tanda petik ganda
    }
}